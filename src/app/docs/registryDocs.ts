function renderModuleTree(): string {
  const pad = (label: string, width: number) => label.padEnd(width);
  const lines: string[] = [];
  lines.push('src/');
  const srcOrder = [
    'kernel', 'data', 'store', 'domains', 'lib', 'app', 'components',
    'hooks', 'layouts', 'types', 'assets', 'test', 'verification', 'workers',
  ];
  for (const name of srcOrder) {
    lines.push(`  ${pad(`${name}/`, 25)}# ${SRC_MODULES[name]}`);
    if (name === 'kernel') {
      for (const [sub, desc] of Object.entries(KERNEL_MODULES)) {
        lines.push(`    ${pad(`${sub}/`, 23)}# ${desc}`);
      }
    }
    if (name === 'domains') {
      for (const [sub, desc] of Object.entries(DOMAIN_MODULES)) {
        lines.push(`    ${pad(`${sub}/`, 23)}# ${desc}`);
      }
    }
  }
  for (const [file, desc] of Object.entries(ENTRY_FILES)) {
    lines.push(`  ${pad(file.replace(/^src\//, ''), 25)}# ${desc}`);
  }
  return lines.join('\n');
}

