export interface GateRow {
  check: string;
  command: string;
  expectation: string;
}

/**
 * Parse the "## The local gate" table out of TESTING.md. Throws (failing
 * the gate test) when the section or table cannot be found — TESTING.md is
 * the canonical source and AGENTS.md must not silently go stale.
 */
export function parseLocalGateTable(testingMd: string): GateRow[] {
  const section = testingMd.split(/^## The local gate.*$/m)[1]?.split(/^## /m)[0];
  if (!section) throw new Error('TESTING.md: "## The local gate" section not found');
  const rows: GateRow[] = [];
  for (const line of section.split('\n')) {
    const m = line.match(/^\|(.+)\|(.+)\|(.+)\|$/);
    if (!m) continue;
    const [check, command, expectation] = [m[1], m[2], m[3]].map((s) => s.trim());
    if (check === 'Check' || /^-+$/.test(check.replace(/\s/g, ''))) continue;
    rows.push({ check, command, expectation });
  }
  if (rows.length < 5) throw new Error('TESTING.md: local-gate table parse found too few rows');
  return rows;
}

