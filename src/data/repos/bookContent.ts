/**
 * The raw IndexedDB rows describing one book's static data. Consumed by
 * app/repositories/BookRepository, which merges in the yjs-backed user
 * inventory to produce a BookMetadata.
 */
export interface ManifestBundle {
  manifest: StaticManifestRow;
  structure: StaticStructureRow | undefined;
  /** Whether the binary EPUB resource is present locally (false ⇒ offloaded). */
  hasResource: boolean;
}

/** The ingestion payload's persisted slice (everything ingest() writes). */
export interface BookIngestData {
  bookId: string;
  manifest: StaticManifestRow & { coverBlob?: Blob | ArrayBuffer };
  resource: { bookId: string; epubBlob: Blob | ArrayBuffer };
  structure: StaticStructureRow;
  ttsContentBatches: CacheTtsPreparationRow[];
  tableBatches: (Omit<TableImageRow, 'imageBlob'> & { imageBlob: Blob | ArrayBuffer })[];
}

/** Derived rows regenerated by a reprocess (replaceDerivedContent). */
interface DerivedContentReplacement {
  /** Updated manifest row (already mutated by the caller); omitted when the book has no manifest. */
  manifest?: StaticManifestRow;
  structure: StaticStructureRow;
  ttsPrep: CacheTtsPreparationRow[];
  tableImages: (Omit<TableImageRow, 'imageBlob'> & { imageBlob: Blob | ArrayBuffer })[];
}

const toArrayBuffer = async (value: Blob | ArrayBuffer): Promise<ArrayBuffer> =>
  value instanceof Blob ? value.arrayBuffer() : Promise.resolve(value);

