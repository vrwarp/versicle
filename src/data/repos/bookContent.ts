  // ── Maintenance (orphan scan/prune over the book-content stores) ───────

  /**
   * Counts records whose parent book is not in `validBookIds`
   * (files, locations, TTS prep). Read-only.
   */
  async scanOrphans(validBookIds: Set<string>): Promise<{ files: number; locations: number; tts_prep: number }> {
    try {
      const db = await getConnection();

      const resourceKeys = await db.getAllKeys('static_resources');
      const orphanedFiles = resourceKeys.filter((k) => !validBookIds.has(k.toString()));

      const metricKeys = await db.getAllKeys('cache_render_metrics');
      const orphanedLocations = metricKeys.filter((k) => !validBookIds.has(k.toString()));

      let orphanedPrep = 0;
      const prepStore = db.transaction('cache_tts_preparation').objectStore('cache_tts_preparation');
      let prepCursor = await prepStore.openCursor();
      while (prepCursor) {
        if (!validBookIds.has(prepCursor.value.bookId)) {
          orphanedPrep++;
        }
        prepCursor = await prepCursor.continue();
      }

      return {
        files: orphanedFiles.length,
        locations: orphanedLocations.length,
        tts_prep: orphanedPrep
      };
    } catch (error) {
      handleDbError(error);
    }
  }

  /**
   * Per-book MINIMUM `extractionVersion` across all `cache_tts_preparation`
   * rows (missing stamp = implicit v1, reported as 1). The Phase 7 §E
   * re-ingest wave's candidacy scan: a single readonly cursor pass.
   */
  async listTtsExtractionVersions(): Promise<Map<string, number>> {
    const result = new Map<string, number>();
    try {
      const db = await getConnection();
      const prepStore = db.transaction('cache_tts_preparation').objectStore('cache_tts_preparation');
      let cursor = await prepStore.openCursor();
      while (cursor) {
        const row = cursor.value;
        const version = row.extractionVersion ?? 1;
        const current = result.get(row.bookId);
        if (current === undefined || version < current) {
          result.set(row.bookId, version);
        }
        cursor = await cursor.continue();
      }
    } catch (error) {
      handleDbError(error);
    }
    return result;
  }

